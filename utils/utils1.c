#include <philo.h>

int	ft_atoi(char *str)
{
	int	res;
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] < '0' || str[i] > '9')
			return (-2);
		i++;
	}
	res = 0;
	while (*str)
	{
		res = 10 * res + *str - '0';
		str++;
	}
	return (res);
}
